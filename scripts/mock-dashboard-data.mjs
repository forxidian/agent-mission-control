const minute = 60 * 1000;
const hour = 60 * minute;

function thread({
  id,
  provider = 'codex',
  providerLabel = 'Codex',
  title,
  projectName,
  cwd,
  model,
  updatedAgoMs,
  tokensUsed,
  todayTokenUsage,
  status = '',
  appDeepLink = '',
  resumeCommand = '',
  openLabel = '打开线程',
  latestUserAgoMs = 0,
  latestFinalAgoMs = 0,
  archived = false,
  hasUnreadTurn = false,
  awaitingPermission = false,
  pendingTools = [],
  todos = [],
  rateLimits = null,
  lastAgentMessage = '',
  lastTokenUsage = null,
}, nowMs) {
  const updatedAtMs = nowMs - updatedAgoMs;
  const latestUserMessageAtMs = latestUserAgoMs ? nowMs - latestUserAgoMs : null;
  const latestAgentFinalAtMs = latestFinalAgoMs ? nowMs - latestFinalAgoMs : null;

  return {
    id,
    externalId: id,
    provider,
    providerLabel,
    title,
    projectName,
    cwd,
    model,
    source: provider,
    tokensUsed,
    todayTokenUsage,
    archived,
    createdAtMs: nowMs - 5 * 24 * hour,
    updatedAtMs,
    latestUserMessageAtMs,
    latestAgentFinalAtMs,
    currentTurnStartedAtMs: status === 'running' ? latestUserMessageAtMs : null,
    latestMessageKind: status === 'running' ? 'user' : 'agent',
    hasUnreadTurn,
    awaitingReview: hasUnreadTurn,
    awaitingPermission,
    pendingTools,
    pendingToolCount: pendingTools.length,
    pendingToolAtMs: pendingTools.length ? updatedAtMs : null,
    openCodePendingTools: pendingTools,
    openCodePendingToolCount: pendingTools.length,
    openCodePendingToolAtMs: pendingTools.length ? updatedAtMs : null,
    todos,
    openCodeTodos: todos,
    rateLimits,
    rateLimitUpdatedAtMs: rateLimits ? updatedAtMs : null,
    appDeepLink,
    canOpen: Boolean(appDeepLink || resumeCommand),
    openLabel,
    resumeCommand,
    rolloutPath: `${cwd}/.agent/mock-rollout.jsonl`,
    lastAgentMessage,
    lastTokenUsage,
  };
}

